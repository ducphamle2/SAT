package ComparisonAlgo.src.com.ducphamle.algo.model;


/**
 *
 * @author Duc Pham Le
 */
public class AlgoModel {
    private int size = 0;
    private double insertionTime = 0;
    private double mergeTime = 0;
    private double quickTime = 0;
    private double bubbleTime = 0;

    
    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public double getInsertionTime() {
        return insertionTime;
    }

    public void setInsertionTime(double insertionTime) {
        this.insertionTime = insertionTime;
    }

    public double getMergeTime() {
        return mergeTime;
    }

    public void setMergeTime(double mergeTime) {
        this.mergeTime = mergeTime;
    }

    public double getQuickTime() {
        return quickTime;
    }

    public void setQuickTime(double quickTime) {
        this.quickTime = quickTime;
    }

    public double getBubbleTime() {
        return bubbleTime;
    }

    public void setBubbleTime(double bubbleTime) {
        this.bubbleTime = bubbleTime;
    }
    
    
}


